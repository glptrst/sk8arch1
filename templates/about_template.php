

<h3>A free tricks database</h3>
<p class="text-info">In Sk8 archives we collect facts of skateboarding history, we're building a collection of the best tricks landed at iconic skate spots. We think that it is easier to collect this data if we do it all togheter. You are welcome to join us and insert tricks that are not present yet.</p>



<h3>Collaborate</h3>
<p class="text-info">Collaboration is the key factor in order to collect the maximum amount of data. If everyone shares his knowledge we will built the best possible database of tricks. So we ask you to insert tricks if you notice any lack. You are also welcome to write us for any comment or suggestion.</p>



<h3>Rules</h3>
<p class="text-info">If you want to insert a trick then you need to provide us with, besides the name of the trick, the name of the skater and the source. The source may be a photo magazine as well as a video, but we can accept something else too (for example a verbal testimonial in a interview).</p>

